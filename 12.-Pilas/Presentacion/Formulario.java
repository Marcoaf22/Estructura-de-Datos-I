    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (m.getRowCount() != 0) {
            int x = p.pop();
            jLabel1.setText("" + x);
            int i = 0;
            while (!p.Vacio()) {
                m.setValueAt(p.pop(), i, m.getColumnCount() - 1);
                i++;
            }
            i = m.getRowCount() - 1;
            while (i >= 0) {
                p.push((int) m.getValueAt(i, 0));
                i--;
            }
            m.setRowCount(m.getRowCount() - 1);
        } else {
            JOptionPane.showMessageDialog(rootPane, "ERROR MEMORIA VACIA");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

