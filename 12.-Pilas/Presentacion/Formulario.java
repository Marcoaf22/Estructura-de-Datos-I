    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked

        if (!p.lleno()) {
            m.setRowCount(m.getRowCount() + 1);
            p.push(Integer.parseInt(jTextField1.getText()));
            jTextField1.setText("");
            int i = 0;
            while (!p.Vacio()) {
                m.setValueAt(p.pop(), i, m.getColumnCount() - 1);
                i++;
            }
            i = m.getRowCount() - 1;
            while (i >= 0) {
                p.push((int) m.getValueAt(i, 0));
                i--;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "ERROR MEMORIA LLENA");
        }
    }//GEN-LAST:event_jButton2MouseClicked

